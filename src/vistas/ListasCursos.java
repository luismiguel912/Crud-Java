    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        dt.setIdCursos(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(),0).toString()));
        dt.setNombres(jTable1.getValueAt(jTable1.getSelectedRow(),1).toString());
        dt.setDescripcion(jTable1.getValueAt(jTable1.getSelectedRow(),2).toString());
      
       
        
        if(jTable1.getSelectedColumn() ==3){
            //Actualizar
            if(JOptionPane.showConfirmDialog(this, "Estas seguro de actualizar los datos","Actualizar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION){
                try {
                    //Llamar  al metedo en sistema facade para actualizar
                    fac.actualizaCursos(dt);
                } catch (SQLException e) {
                    Logger.getLogger(ListasCursos.class.getName()).log(Level.SEVERE, null,e);
                }catch (Exception e) {
                    Logger.getLogger(ListasCursos.class.getName()).log(Level.SEVERE, null,e);
                }
                
            }    
        }
        
        if(jTable1.getSelectedColumn() == 4){
            //Eliminar
            if(JOptionPane.showConfirmDialog(this, "Estas seguro de Eliminar los datos","Elimnar",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION){
                try {
                    //Llamar  al metedo en sistema facade para actualizar
                    fac.eliminarCursos(dt);
                    llenado();
                } catch (SQLException e) {
                    Logger.getLogger(ListasCursos.class.getName()).log(Level.SEVERE, null,e);
                }catch (Exception e) {
                    Logger.getLogger(ListasCursos.class.getName()).log(Level.SEVERE, null,e);
                }
                
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked


