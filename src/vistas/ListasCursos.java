     public void llenado(){
        String[]columnas={"Clave Curso","Nombres","Descripcion","Actualizar","Eliminar"};
        List lista;
        int fila =-1;
        try {
            Object[][] data= new Object[fac.listaCursos().size()][5];
            lista=fac.listaCursos();
            //Actualizar
            Iterator it=lista.iterator();
            while(it.hasNext()){
                dt = (CursosDT) it.next();
                fila++;
            data[fila][0] = dt.getIdCursos();
            data[fila][1] = dt.getNombres();
            data[fila][2] = dt.getDescripcion();
            data[fila][3]="Actualizar";
            data[fila][4]="Eliminar";
                
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(data,columnas));
        } catch (Exception e) {
            Logger.getLogger(ListasCursos.class.getName()).log(Level.SEVERE, null,e);
        }
    }

