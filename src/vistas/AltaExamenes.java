    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //codigo para validar o dar mensaje de campos vacios (ButtonGuardar-events-ActionPerformed) 

            if(txtNumPreguntas.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Falta campos por llenar!!!","error",JOptionPane. ERROR_MESSAGE);
          }
            else if(txtNombreExamen.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Falta campos por llenar!!!","error",JOptionPane. ERROR_MESSAGE);
          }
            else if(txtIdProfesores.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Falta campos por llenar!!!","error",JOptionPane. ERROR_MESSAGE);
          }
            else if(txtIdAsignaturas.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Falta campos por llenar!!!","error",JOptionPane. ERROR_MESSAGE);
          }
            else if(txtIdCursos.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Falta campos por llenar!!!","error",JOptionPane. ERROR_MESSAGE);
          }
            
             else{
        
          try {
            if(dt != null){
                
                String dia = cbxDia.getSelectedItem().toString();
                String mes = cbxMes.getSelectedItem().toString();
                String a単o = cbxA単o.getSelectedItem().toString();
                SimpleDateFormat df = new SimpleDateFormat("dd-MMMMM-yyyy");

                String fecha = dia+"-"+mes+"-"+ a単o;
                long Fecha = df.parse(fecha).getTime();
                Date fech = new Date (Fecha);
                dt.setFecha(fech);

                dt.setNumPreguntas(Integer.parseInt(txtNumPreguntas.getText()));
                txtNumPreguntas.setText("");
                dt.setNombreExamen(txtNombreExamen.getText());
                txtNombreExamen.setText("");
                dt.setIdProfesores(Integer.parseInt(txtIdProfesores.getText()));
                txtIdProfesores.setText("");
                dt.setIdAsignaturas(Integer.parseInt(txtIdAsignaturas.getText()));
                txtIdAsignaturas.setText("");
                dt.setIdCursos(Integer.parseInt(txtIdCursos.getText()));
                txtIdCursos.setText("");
                fac.nuevoExamenes(dt);
                
            }else{

            }
            JOptionPane.showMessageDialog(this, "Datos guardados");
        } catch (Exception e) {
            Logger.getLogger(AltaAlumnos.class.getName()).log(Level.SEVERE,null, e);
        }
            }
    }//GEN-LAST:event_jButton1ActionPerformed

