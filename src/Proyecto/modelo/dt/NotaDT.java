package Proyecto.modelo.dt;

/**
 *
 * @author luis
 */
public class NotaDT {
    private int idNota;
    private int notas;
    private int idAlumnos;
    private int idExamenes;

    /**
     * @return the idNota
     */
    public int getIdNota() {
        return idNota;
    }

    /**
     * @param idNota the idNota to set
     */
    public void setIdNota(int idNota) {
        this.idNota = idNota;
    }

    /**
     * @return the notas
     */
    public int getNotas() {
        return notas;
    }

    /**
     * @param notas the notas to set
     */
    public void setNotas(int notas) {
        this.notas = notas;
    }

    /**
     * @return the idAlumnos
     */
    public int getIdAlumnos() {
        return idAlumnos;
    }

    /**
     * @param idAlumnos the idAlumnos to set
     */
    public void setIdAlumnos(int idAlumnos) {
        this.idAlumnos = idAlumnos;
    }

    /**
     * @return the idExamenes
     */
    public int getIdExamenes() {
        return idExamenes;
    }

    /**
     * @param idExamenes the idExamenes to set
     */
    public void setIdExamenes(int idExamenes) {
        this.idExamenes = idExamenes;
    }
    
}
